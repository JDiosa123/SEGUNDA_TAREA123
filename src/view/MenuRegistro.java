    private void btEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEstudianteActionPerformed
        // TODO add your handling code here:
String codigo = txtCodEstudiante.getText();
    String nombre = txtNomEstudiante.getText();

    if (codigo.isEmpty() || nombre.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Completa todos los campos del estudiante.");
        return;
    }

    // Insertar en base de datos
    boolean exito = new PanelEstudiantesBD().insertarEstudiante(codigo, nombre);
    if (exito) {
        modeloEstudiantes.addRow(new Object[]{codigo, nombre}); // Agrega al JTable
        txtCodEstudiante.setText("");
        txtNomEstudiante.setText("");
        llenarComboEstudiante();
        JOptionPane.showMessageDialog(null, "Estudiante agregado correctamente.");
    } else {
        JOptionPane.showMessageDialog(null, "Error al agregar el estudiante.");
    }
    }//GEN-LAST:event_btEstudianteActionPerformed

    private void btDocenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDocenteActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodDocente.getText();
        String nombre = txtNomDocente.getText();

        if (codigo.isEmpty() || nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completa todos los campos del docente.");
            return;
        }

        modeloDocentes.addRow(new Object[]{codigo, nombre});
        new PanelDocentesBD().insertarDocente(codigo, nombre);
        llenarComboDocente(); // actualizar combo
        txtCodDocente.setText("");
        txtNomDocente.setText("");
        
    }//GEN-LAST:event_btDocenteActionPerformed

    private void btCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCursoActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodCurso.getText();
        String nombre = txtNomCurso.getText();
        String docente = (String) jComboBoxDocente.getSelectedItem();

        if (codigo.isEmpty() || nombre.isEmpty() || docente == null || docente.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completa todos los campos del curso.");
            return;
        }

        modeloCursos.addRow(new Object[]{codigo, nombre, docente});
        new PanelCursosBD().insertarCurso(codigo, nombre, docente);
        llenarComboCurso(); // actualizar combo
        txtCodCurso.setText("");
        txtNomCurso.setText("");
    }//GEN-LAST:event_btCursoActionPerformed

    private void btMatriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMatriculaActionPerformed
        // TODO add your handling code here:
        String estudiante = (String) jComboBoxEstudiante.getSelectedItem();
        String curso = (String) jComboBoxCurso.getSelectedItem();
        String notaStr = txtNota.getText();

        if (estudiante == null || curso == null || notaStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completa todos los campos de matr铆cula.");
            return;
        }

        try {
            double nota = Double.parseDouble(notaStr);
            modeloMatricula.addRow(new Object[]{estudiante, curso, nota});
            new PanelMatriculaBD().insertarMatricula(estudiante, curso, nota);
            txtNota.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "La nota debe ser num茅rica.");
        }
    }//GEN-LAST:event_btMatriculaActionPerformed

